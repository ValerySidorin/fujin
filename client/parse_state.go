type parseState struct {
	state      int
	argBuf     []byte
	metaBuf    []byte
	payloadBuf []byte

	ea errArg
	ma msgArg
	fa fetchArg
	aa ackArg
	ca correlationIDArg
}

type correlationIDArg struct {
	cID       []byte
	cIDUint32 uint32
}

type msgArg struct {
	sub   *Subscription
	idLen uint32
	len   uint32
}

type fetchArg struct {
	autoCommit bool
	n          uint32
	handled    uint32
	msgs       []Msg
	err        chan error
}

type ackArg struct {
	n            uint32
	currMsgIDLen uint32
	resps        []AckMsgResponse
}

type errArg struct {
	errLen uint32
}
